# 6. 2つのデータセットの結合（full_join）
#==============================================================================
# データセットA：店舗単位（ID・都道府県・店舗平均点数）
dataset_A <- tribble(
  ~ID    , ~都道府県 , ~店舗平均点数 ,
  "S001" , "京都府"   , 4.2                 ,
  "S002" , "京都府"   , 3.8                 ,
  "S003" , "大阪府"   , 4.0                 ,
  "S004" , "兵庫県"   , 3.5
)

# データセットB：県ごとの価格情報（ID・都道府県・県内平均価格）
# ※ 和歌山県は A にない／兵庫県は B にない → 片方にしかない県の行も残る
dataset_B <- tribble(
  ~ID   , ~都道府県  , ~県内平均価格 ,
  "P01" , "京都府"    ,                1200 ,
  "P02" , "大阪府"    ,                 950 ,
  "P03" , "和歌山県" ,                 880
)

# データセットの確認
print(dataset_A)
print(dataset_B)

# 都道府県をキーに結合（両方にしかない県の行も残す）
dataset_A_B <- full_join(dataset_A, dataset_B, by = "都道府県")
print(dataset_A_B)

#必要な項目だけにする
dataset_A_B_select <- dataset_A_B %>%
  select(ID=ID.x,　都道府県, 店舗平均点数, 県内平均価格)

print(dataset_A_B_select)

