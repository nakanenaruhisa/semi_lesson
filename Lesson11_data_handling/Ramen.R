Ramen <- read_csv("Lesson11_data_handling/Ramen.csv")



#==============================================================================
# 2. 初期確認
#==============================================================================
head(Ramen)  # 前 5 行を表示
str(Ramen)   # データ構造を確認（タイプ・次元）
summary(Ramen)  # 要約統計量









#==============================================================================
# 3. 列選択（select() で「変数」を抽出）
#==============================================================================
Ramen_select <- Ramen %>%
  select(ID, Pref, Score, Name)



head(Ramen_select)  # 確認









#==============================================================================
# 4. フィルタリング（京都・和歌山）
#==============================================================================
Ramen_kyoto_wakayama <- Ramen_select %>%

  filter(Pref %in% c("京都府", "和歌山県"))


print(Ramen_kyoto_wakayama)



#==============================================================================
# 5. NA 値の確認・排除
#==============================================================================
Ramen_complete <- Ramen_kyoto_wakayama %>%
  filter(!is.na(Score))



print(Ramen_complete)






